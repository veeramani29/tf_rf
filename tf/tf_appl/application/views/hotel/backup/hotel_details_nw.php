$(document).ready(function() {
        $(window).scroll(function() {
			var winTop = $(this).scrollTop();
			var minusht = $(window).height() / 2;
			var $loaddiv = $('.scroll');
			
			var top = $.grep($loaddiv, function(item) {
				return $(item).position().top <= winTop + minusht;
			});
		
			$loaddiv.removeClass('active');
			$(top).addClass('active');
		});
});
